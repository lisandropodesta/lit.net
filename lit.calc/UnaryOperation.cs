namespace Lit.calc
{
    public enum UnaryOperation
    {
        /// <summary>
        /// Self value.
        /// </summary>
        Identity,

        /// <summary>
        /// Negated value.
        /// </summary>
        Negate,

        /// <summary>
        /// Absolute value.
        /// </summary>
        Abs,

        /// <summary>
        /// Binary complement value.
        /// </summary>
        Complement,

        /// <summary>
        /// Trigonometric sine.
        /// </summary>
        Sin,

        /// <summary>
        /// Trigonometric cosine.
        /// </summary>
        Cos,

        /// <summary>
        /// Trigonometric tangent.
        /// </summary>
        Tan,

        /// <summary>
        /// Natural logarithm.
        /// </summary>
        Ln,

        /// <summary>
        /// Decimal logarithm.
        /// </summary>
        Log10
    }
}
